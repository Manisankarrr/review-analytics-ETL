@app.get("/summary", response_model=SummaryOut, tags=["Analytics"])
def get_summary(
    app_id: Optional[str] = Query(None, description="Filter by Google Play app id"),
    db: Session = Depends(get_db),
) -> SummaryOut:
    """Headline KPIs. Backs the Executive Overview page."""
    try:
        query = select(
            func.count().label("total"),
            func.avg(Review.rating).label("avg_rating"),
            func.sum(case((Review.sentiment_label == "Positive", 1), else_=0)).label("pos"),
            func.sum(case((Review.sentiment_label == "Neutral", 1), else_=0)).label("neu"),
            func.sum(case((Review.sentiment_label == "Negative", 1), else_=0)).label("neg"),
        )
        if app_id:
            query = query.where(Review.app_id == app_id)

        row = db.execute(query).one()
        total = row.total or 0

        if total == 0:
            return SummaryOut(
                total_reviews=0,
                average_rating=0.0,
                positive_count=0,
                neutral_count=0,
                negative_count=0,
                positive_pct=0.0,
                neutral_pct=0.0,
                negative_pct=0.0,
            )

        pos, neu, neg = row.pos or 0, row.neu or 0, row.neg or 0
        return SummaryOut(
            total_reviews=total,
            average_rating=round(float(row.avg_rating or 0.0), 2),
            positive_count=pos,
            neutral_count=neu,
            negative_count=neg,
            positive_pct=round(100 * pos / total, 2),
            neutral_pct=round(100 * neu / total, 2),
            negative_pct=round(100 * neg / total, 2),
        )
    except Exception:
        logger.exception("Failed to compute summary.")
        raise HTTPException(status_code=500, detail="Failed to compute summary.")


