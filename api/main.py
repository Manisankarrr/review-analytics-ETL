@app.get(
    "/sentiment-trend", response_model=List[SentimentTrendPoint], tags=["Analytics"]
)
def get_sentiment_trend(
    app_id: Optional[str] = Query(None, description="Filter by Google Play app id"),
    db: Session = Depends(get_db),
) -> List[SentimentTrendPoint]:
    """Daily positive/neutral/negative review counts. Backs trend charts."""
    try:
        day_expr = func.date(Review.review_date)
        query = (
            select(
                day_expr.label("day"),
                func.sum(case((Review.sentiment_label == "Positive", 1), else_=0)).label(
                    "positive"
                ),
                func.sum(case((Review.sentiment_label == "Neutral", 1), else_=0)).label(
                    "neutral"
                ),
                func.sum(case((Review.sentiment_label == "Negative", 1), else_=0)).label(
                    "negative"
                ),
            )
            .group_by(day_expr)
            .order_by(day_expr)
        )
        if app_id:
            query = query.where(Review.app_id == app_id)

        rows = db.execute(query).all()
        return [
            SentimentTrendPoint(
                date=row.day, positive=row.positive, neutral=row.neutral, negative=row.negative
            )
            for row in rows
        ]
    except Exception:
        logger.exception("Failed to compute sentiment trend.")
        raise HTTPException(status_code=500, detail="Failed to compute sentiment trend.")


