@app.get("/etl-stats", response_model=EtlStatsOut, tags=["Data Quality"])
def get_etl_stats(
    history: int = Query(10, ge=1, le=100, description="Number of recent runs to return"),
    db: Session = Depends(get_db),
) -> EtlStatsOut:
    """Latest + recent ETL run statistics. Backs the Data Quality page."""
    try:
        query = select(EtlRun).order_by(EtlRun.run_timestamp.desc()).limit(history)
        rows = db.execute(query).scalars().all()
        runs = [EtlRunOut.model_validate(r) for r in rows]
        return EtlStatsOut(latest_run=runs[0] if runs else None, recent_runs=runs)
    except Exception:
        logger.exception("Failed to retrieve ETL stats.")
        raise HTTPException(status_code=500, detail="Failed to retrieve ETL stats.")