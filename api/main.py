"""
api/main.py
============
FastAPI service exposing the review analytics data over REST. This is the
ONLY component the Streamlit dashboard is allowed to talk to — it never
queries MySQL directly.

Run from the project root:
    uvicorn api.main:app --reload --host 0.0.0.0 --port 8000

Interactive docs are auto-generated by FastAPI at /docs (Swagger UI) and
/redoc.
"""

