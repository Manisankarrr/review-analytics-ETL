@app.get("/top-keywords", response_model=List[KeywordOut], tags=["Analytics"])
def get_top_keywords(
    sentiment_label: Optional[str] = Query(
        None, description="Positive, Neutral, or Negative. Omit for all."
    ),
    app_id: Optional[str] = Query(None, description="Filter by Google Play app id"),
    limit: int = Query(20, ge=1, le=100),
    db: Session = Depends(get_db),
) -> List[KeywordOut]:
    """Top keywords by cumulative frequency. Backs Keyword Intelligence page."""
    if sentiment_label and sentiment_label not in {"Positive", "Neutral", "Negative"}:
        raise HTTPException(
            status_code=400,
            detail="sentiment_label must be one of: Positive, Neutral, Negative",
        )
    try:
        query = select(Keyword)
        if sentiment_label:
            query = query.where(Keyword.sentiment_label == sentiment_label)
        if app_id:
            query = query.where(Keyword.app_id == app_id)
        query = query.order_by(Keyword.frequency.desc()).limit(limit)

        rows = db.execute(query).scalars().all()
        return [KeywordOut(keyword=r.keyword, frequency=r.frequency) for r in rows]
    except HTTPException:
        raise
    except Exception:
        logger.exception("Failed to retrieve top keywords.")
        raise HTTPException(status_code=500, detail="Failed to retrieve top keywords.")


