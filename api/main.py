@app.post("/apps/analyze", response_model=AnalyzeAppResponse, tags=["Ingestion"])
def analyze_app(payload: AnalyzeAppRequest) -> AnalyzeAppResponse:    
    try:
        app_id = extract_app_id(payload.input)
    except InvalidAppIdentifierError as exc:
        raise HTTPException(status_code=400, detail=str(exc))

    settings = get_settings()

    try:
        raw_df = extract_stage.run_extraction(settings, app_ids=[app_id])
    except Exception:
        logger.exception("Extraction failed for app_id=%s", app_id)
        raise HTTPException(
            status_code=502,
            detail=f"Failed to fetch reviews for '{app_id}' from Google Play.",
        )

    if raw_df.empty:
        raise HTTPException(
            status_code=404,
            detail=(
                f"No reviews were found for app id '{app_id}'. "
                "It may not exist on Google Play, or has no reviews in the "
                "configured language/country."
            ),
        )

    clean_df, keywords_df, stats = transform_stage.transform(raw_df)
    stats["app_ids"] = app_id

    try:
        load_counts = load_stage.load_dataframes([app_id], clean_df, keywords_df, stats)
    except Exception:
        logger.exception("Load failed for app_id=%s", app_id)
        raise HTTPException(
            status_code=500,
            detail="Failed to load the analyzed reviews into the database.",
        )

    return AnalyzeAppResponse(
        app_id=app_id,
        status="SUCCESS",
        raw_count=stats.get("raw_count", 0),
        final_row_count=stats.get("final_row_count", 0),
        new_reviews_inserted=load_counts.get("new_reviews_inserted", 0),
        duplicate_reviews_skipped=load_counts.get("duplicate_reviews_skipped", 0),
        message=(
            f"Analyzed {stats.get('final_row_count', 0)} reviews for {app_id} "
            f"({load_counts.get('new_reviews_inserted', 0)} new)."
        ),
    )


