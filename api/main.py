@app.on_event("startup")
def on_startup() -> None:
    """Fail fast and loudly if required configuration is missing."""
    try:
        get_settings()
    except ConfigError as exc:
        logger.error("Configuration error at startup: %s", exc)
        raise
    logger.info("API startup complete. Configuration validated.")


