_VALID_SORT_FIELDS = {
    "review_date": Review.review_date,
    "rating": Review.rating,
    "sentiment_score": Review.sentiment_score,
    "thumbs_up_count": Review.thumbs_up_count,
}


@app.get("/reviews", response_model=PaginatedReviews, tags=["Reviews"])
def list_reviews(
    page: int = Query(1, ge=1, description="1-indexed page number"),
    page_size: int = Query(20, ge=1, le=200, description="Rows per page"),
    sentiment_label: Optional[str] = Query(
        None, description="Filter by Positive, Neutral, or Negative"
    ),
    rating: Optional[int] = Query(None, ge=1, le=5, description="Exact rating filter"),
    min_rating: Optional[int] = Query(None, ge=1, le=5),
    max_rating: Optional[int] = Query(None, ge=1, le=5),
    app_id: Optional[str] = Query(None, description="Filter by Google Play app id"),
    search: Optional[str] = Query(
        None, description="Free-text search within review text"
    ),
    sort_by: str = Query("review_date", description="One of: " + ", ".join(_VALID_SORT_FIELDS)),
    order: str = Query("desc", pattern="^(asc|desc)$"),
    db: Session = Depends(get_db),
) -> PaginatedReviews:
    """
    Paginated, filterable, sortable review listing. Backs the Review
    Explorer dashboard page.
    """
    if sort_by not in _VALID_SORT_FIELDS:
        raise HTTPException(
            status_code=400,
            detail=f"sort_by must be one of: {', '.join(_VALID_SORT_FIELDS)}",
        )
    if sentiment_label and sentiment_label not in {"Positive", "Neutral", "Negative"}:
        raise HTTPException(
            status_code=400,
            detail="sentiment_label must be one of: Positive, Neutral, Negative",
        )
    if min_rating is not None and max_rating is not None and min_rating > max_rating:
        raise HTTPException(
            status_code=400, detail="min_rating cannot exceed max_rating"
        )

    try:
        query = select(Review)

        if sentiment_label:
            query = query.where(Review.sentiment_label == sentiment_label)
        if rating is not None:
            query = query.where(Review.rating == rating)
        if min_rating is not None:
            query = query.where(Review.rating >= min_rating)
        if max_rating is not None:
            query = query.where(Review.rating <= max_rating)
        if app_id:
            query = query.where(Review.app_id == app_id)
        if search:
            query = query.where(Review.review_text.ilike(f"%{search}%"))

        count_query = select(func.count()).select_from(query.subquery())
        total = db.execute(count_query).scalar_one()

        sort_column = _VALID_SORT_FIELDS[sort_by]
        sort_column = sort_column.desc() if order == "desc" else sort_column.asc()
        query = query.order_by(sort_column)

        query = query.offset((page - 1) * page_size).limit(page_size)
        rows = db.execute(query).scalars().all()

        total_pages = math.ceil(total / page_size) if total else 0

        return PaginatedReviews(
            total=total,
            page=page,
            page_size=page_size,
            total_pages=total_pages,
            items=[ReviewOut.model_validate(r) for r in rows],
        )
    except HTTPException:
        raise
    except Exception:
        logger.exception("Failed to list reviews.")
        raise HTTPException(status_code=500, detail="Failed to retrieve reviews.")


